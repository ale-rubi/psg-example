ejemplo = "La llama que llama"

print("Funcion Index")
print (ejemplo.index("la"))

print("Funcion RIndex")
print (ejemplo.rindex("la"))

print("Funcion removeprefix")
print (ejemplo.removeprefix('La '))

print("Funcion removesuffix")
print (ejemplo.removesuffix('ma'))

print("Funcion partition")
print (ejemplo.partition("llama"))

